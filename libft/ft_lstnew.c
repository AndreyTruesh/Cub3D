#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*tmp;

	tmp = malloc(sizeof(t_list));
	if (!tmp)
		return (void *)0;
	tmp->next = (void *)0;
	tmp->content = content;
	return (tmp);
}
